def generate_expert_explanation(goal: str, calories: int, missing_ingredients: list) -> str:
    explanation = "\n💡 **Експертний висновок:** "
    goal_lower = goal.lower()

    # Логіка для схуднення
    if goal_lower == 'схуднення':
        if calories < 350:
            explanation += "Ця страва ідеально підходить для схуднення. Вона має низьку калорійність, що допоможе вам легко тримати дефіцит без відчуття голоду. "
        else:
            explanation += "Це досить ситний прийом їжі. Рекомендую обрати його на обід, щоб зберегти енергію до кінця дня, залишаючись у межах вашого дефіциту. "

    # Логіка для набору маси
    elif goal_lower in ['набір', 'набір маси']:
        if calories >= 450:
            explanation += "Відмінний вибір для набору маси! Висока калорійність та поживна цінність забезпечать необхідний профіцит для росту м'язів. "
        else:
            explanation += "Це легка страва. Оскільки ваша мета — набір маси, рекомендую додати до неї горіхи, сир або просто збільшити порцію на 30%. "

    # Логіка для підтримки
    else:
        explanation += "Збалансована страва, яка чудово підійде для підтримки поточної форми та здорового метаболізму. "

    # Обробка нестачі інгредієнтів
    if missing_ingredients:
        missed_str = ", ".join(missing_ingredients)
        explanation += f"\n🛒 *Чого не вистачає:* {missed_str}. (Але страву можна адаптувати без них або замінити аналогами)."
    else:
        explanation += "\n✨ *У вас є всі 100% необхідних інгредієнтів!*"

    return explanation